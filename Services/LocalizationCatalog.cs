    private static readonly IReadOnlyDictionary<string, string> English =
        French.GroupBy(pair => pair.Value, StringComparer.Ordinal)
            .ToDictionary(group => group.Key, group => group.First().Key, StringComparer.Ordinal);

    private static readonly Template[] Templates = French
        .Where(pair => pair.Key.Contains('{'))
        .OrderByDescending(pair => pair.Key.Length)
        .Select(pair => Template.Create(pair.Key, pair.Value))
        .ToArray();

    public static bool ContainsEnglish(string value) => French.ContainsKey(value);
    public static bool TryGetFrench(string english, out string french) => French.TryGetValue(english, out french!);
    public static bool TryGetEnglish(string french, out string english) => English.TryGetValue(french, out english!);

    public static bool TryGetFrenchTemplate(string value, out string french)
    {
        foreach (var template in Templates)
        {
            var match = template.Pattern.Match(value);
            if (!match.Success) continue;
            french = Regex.Replace(template.FrenchFormat, @"\{(\d+)(?:[^}]*)\}",
                placeholder => match.Groups["p" + placeholder.Groups[1].Value].Value);
            return true;
        }
        french = "";
        return false;
    }

    private sealed record Template(Regex Pattern, string FrenchFormat)
    {
        private static readonly Regex Placeholder = new(@"\{(\d+)(?:[^}]*)\}", RegexOptions.CultureInvariant);

        public static Template Create(string englishFormat, string frenchFormat)
        {
            var pattern = new StringBuilder("^");
            var position = 0;
            var groups = new HashSet<string>(StringComparer.Ordinal);
            foreach (Match placeholder in Placeholder.Matches(englishFormat))
            {
                pattern.Append(Regex.Escape(englishFormat[position..placeholder.Index]));
                var group = "p" + placeholder.Groups[1].Value;
                pattern.Append(groups.Add(group) ? $"(?<{group}>.*?)" : $"\\k<{group}>");
                position = placeholder.Index + placeholder.Length;
            }
            pattern.Append(Regex.Escape(englishFormat[position..]));
            pattern.Append('$');
            return new Template(new Regex(pattern.ToString(), RegexOptions.Singleline | RegexOptions.CultureInvariant), frenchFormat);
        }
    }
